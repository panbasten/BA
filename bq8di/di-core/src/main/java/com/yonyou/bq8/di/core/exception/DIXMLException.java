public class DIXMLException extends DIException {

	private static final long serialVersionUID = -3906432363473258830L;

	public DIXMLException() {
		super();
	}

	public DIXMLException(String message) {
		super(message);
	}

	public DIXMLException(Throwable cause) {
		super(cause);
	}

	public DIXMLException(String message, Throwable cause) {
		super(message, cause);
	}
}
