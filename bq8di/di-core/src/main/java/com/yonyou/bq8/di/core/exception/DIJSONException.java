package com.yonyou.bq8.di.core.exception;

public class DIJSONException extends DIException {

	private static final long serialVersionUID = -1513791894680527974L;

	public DIJSONException() {
		super();
	}

	public DIJSONException(String message) {
		super(message);
	}

	public DIJSONException(Throwable cause) {
		super(cause);
	}

	public DIJSONException(String message, Throwable cause) {
		super(message, cause);
	}

}
