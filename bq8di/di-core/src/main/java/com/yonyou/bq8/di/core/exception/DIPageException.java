package com.yonyou.bq8.di.core.exception;

public class DIPageException extends DIException{

	private static final long serialVersionUID = -3434002663924436160L;

	public DIPageException() {
		super();
	}

	public DIPageException(String message) {
		super(message);
	}

	public DIPageException(Throwable cause) {
		super(cause);
	}

	public DIPageException(String message, Throwable cause) {
		super(message, cause);
	}
	
}
