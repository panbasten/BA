package com.yonyou.bq8.di.core.exception;

public class DIConvertException extends DIException {

	private static final long serialVersionUID = -1513791894680527974L;

	public DIConvertException() {
		super();
	}

	public DIConvertException(String message) {
		super(message);
	}

	public DIConvertException(Throwable cause) {
		super(cause);
	}

	public DIConvertException(String message, Throwable cause) {
		super(message, cause);
	}

}
