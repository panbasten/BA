package com.flywet.platform.bi.core.exception;

public class BIPageException extends BIException{

	private static final long serialVersionUID = -3434002663924436160L;

	public BIPageException() {
		super();
	}

	public BIPageException(String message) {
		super(message);
	}

	public BIPageException(Throwable cause) {
		super(cause);
	}

	public BIPageException(String message, Throwable cause) {
		super(message, cause);
	}
	
}
