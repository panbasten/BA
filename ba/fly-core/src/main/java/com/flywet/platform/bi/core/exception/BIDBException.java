package com.flywet.platform.bi.core.exception;

public class BIDBException extends BIException {

	private static final long serialVersionUID = -1417354384091639754L;

	public BIDBException() {
		super();
	}

	public BIDBException(String message) {
		super(message);
	}

	public BIDBException(Throwable cause) {
		super(cause);
	}

	public BIDBException(String message, Throwable cause) {
		super(message, cause);
	}
}
