package com.plywet.platform.bi.core.exception;

public class BIJSONException extends BIException {

	private static final long serialVersionUID = -1513791894680527974L;

	public BIJSONException() {
		super();
	}

	public BIJSONException(String message) {
		super(message);
	}

	public BIJSONException(Throwable cause) {
		super(cause);
	}

	public BIJSONException(String message, Throwable cause) {
		super(message, cause);
	}

}
