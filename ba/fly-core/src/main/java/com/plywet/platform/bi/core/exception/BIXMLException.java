package com.plywet.platform.bi.core.exception;

public class BIXMLException extends BIException {

	private static final long serialVersionUID = -3906432363473258830L;

	public BIXMLException() {
		super();
	}

	public BIXMLException(String message) {
		super(message);
	}

	public BIXMLException(Throwable cause) {
		super(cause);
	}

	public BIXMLException(String message, Throwable cause) {
		super(message, cause);
	}
}
