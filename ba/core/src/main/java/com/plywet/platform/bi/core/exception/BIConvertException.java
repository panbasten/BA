package com.plywet.platform.bi.core.exception;

public class BIConvertException extends BIException {

	private static final long serialVersionUID = -1513791894680527974L;

	public BIConvertException() {
		super();
	}

	public BIConvertException(String message) {
		super(message);
	}

	public BIConvertException(Throwable cause) {
		super(cause);
	}

	public BIConvertException(String message, Throwable cause) {
		super(message, cause);
	}

}
